void loop() {

  int availableBytes = Serial.available();
  if (availableBytes>0){
    strLen = 0;
    for(int i=0; i<availableBytes; i++) {
      char a = Serial.read();
      if((a>=20)&&(a<=120)){
        str[strLen] = a;
        strLen++;
      }
    }
    ptrChar = 0;
    scrollPosition = 0;
  }

  updateDisplay();
  doScrolling();
  //drawWall();
  delay(120);
}

void doScrolling(){

  for(int k = FIELD_HEIGHT - 1; k > 0; k-- ){
    for(int j=0; j < FIELD_WIDTH; j++){
      wall[j][k] = wall[j][k-1];
    }
  }

  ptrChar = scrollPosition/8;
  byte charLine = 7 - (scrollPosition-(8 * ptrChar));
  char c = str[ptrChar];

  // Serial.print("Scroll:\t");
  // Serial.print(scrollPosition);
  // Serial.print("\tChar pos:\t");
  // Serial.print(ptrChar);
  // Serial.print("\tLine:\t");
  // Serial.print(charLine);
  // Serial.print("\tChar:\t");
  // Serial.print(c);
  // Serial.print('\t');
  // Serial.println(c,HEX);

  byte bitMask = 0x01;
  byte f = font[c][charLine];

  if((c == ' ') && (charLine == 0)){
    colorLeters++;
  }
  if (colorLeters > brick_count - 1){
    colorLeters = 0;
  }

  uint8_t color = pgm_read_byte(&(brick_colors[colorLeters]));

  for (int x = 0; x < 8; x++) {
    if (f & bitMask){
      Serial.print('O');
      wall[x][0] = color;
    }else{
      wall[x][0] = 0;
      Serial.print('.');
    }
    bitMask = bitMask << 1;
  }
  Serial.println('.');

  for( byte i = 0; i < FIELD_WIDTH; i++ ){
    for( byte k = 0; k < FIELD_HEIGHT; k++ ){
      draw(wall[i][k],FULL,i,k);
    }
  }

  scrollPosition++;
  if (scrollPosition > (strLen * 8) - 1){
    scrollPosition = 0;
  }
}


void setupChar(){
  char c = str[ptrChar];

  Serial.print(c);
  Serial.print('\t');
  Serial.println(c,HEX);

  if(c ==  ' '){
    colorLeters++;
  }

  for (int x = 0; x < 8; x++) {
    byte bitMask = 0x01;
    byte f = font[c][x];
    for (int y = 0; y < 8; y++) {
      if (f & bitMask){
        pixels[x][y] = LOW;
      }else{
        pixels[x][y] = HIGH;
      }
      bitMask = bitMask << 1;
    }
  }

  ptrChar++;
  if(ptrChar>=strLen){
    ptrChar = 0;
  }
}

//draws wall only, does not update display
void drawWall(){
  for(int j=0; j < 8; j++){
    for(int k = 0; k < 8; k++ )
    {
      int thisPixel = pixels[k][j];
      if (thisPixel == LOW) {
        uint8_t c = pgm_read_byte(&(brick_colors[colorLeters]));
        draw(c,FULL,j,k);
      }else{
        draw(0,FULL,j,k);
      }
    }
  }
}

//Update LED strips
void updateDisplay(){
  strip.show();
}

